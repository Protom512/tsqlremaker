//! JavaScript-friendly token representations

use tsql_lexer::Token as LexerToken;
use tsql_token::{Position, Span, TokenKind};

/// JavaScript-friendly token
#[cfg(feature = "wasm")]
#[derive(Debug, Clone, serde::Serialize, serde::Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct JsToken {
    /// Token kind
    pub kind: JsTokenKind,
    /// Token text
    pub text: String,
    /// Position information
    pub span: JsSpan,
}

#[cfg(feature = "wasm")]
impl From<LexerToken<'_>> for JsToken {
    fn from(token: LexerToken<'_>) -> Self {
        Self {
            kind: JsTokenKind::from(token.kind),
            text: token.text.to_string(),
            span: JsSpan::from(token.span),
        }
    }
}

